    public static void atualizarTabela() {
        
        tabelaCompras.removeAll();
        
        ProdutoDAO produtoDAO = new ProdutoDAO();
        
        int cont = 0;
        for (Produtos produto : produtoDAO.listar()) {
            tabelaCompras.setValueAt(produto.getCodigo(), cont, 0);
            tabelaCompras.setValueAt(produto.getDescricao(), cont, 1);
            tabelaCompras.setValueAt(produto.getQuantidade(), cont, 2);
            tabelaCompras.setValueAt(produto.getPreco(), cont, 3);
            tabelaCompras.setValueAt(produto.getFornecedor(), cont, 4);
            tabelaCompras.setValueAt(produto.getEstoque(), cont, 5);
            cont++;
        }
        
    }
    
