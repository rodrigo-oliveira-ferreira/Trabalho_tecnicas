    public static void atualizarTabela() {
        
        tabelaEstoque.removeAll();
        
        EstoqueDAO estoqueDAO = new EstoqueDAO();
        
        int cont = 0;
        for (Estoque estoque : estoqueDAO.listar()) {
            tabelaEstoque.setValueAt(estoque.getCodigo(), cont, 0);
            tabelaEstoque.setValueAt(estoque.getLoja(), cont, 1);
            cont++;
        }
        
    }

