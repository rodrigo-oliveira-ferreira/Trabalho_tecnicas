    private void estoqueBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_estoqueBtnActionPerformed
        try{
        int c = Integer.parseInt(JOptionPane.showInputDialog(null, "Qual estoque você deseja acessar?"));
        
        EstoqueDAO estoqueDAO = new EstoqueDAO();
        
        if (estoqueDAO.existsEstoque(c)) {
            codigoProdutoEstoque.codigoProduto = c;
            setVisible(false);
            new TIEstoque().setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "O estoque não existe.");
        }
        }catch(NumberFormatException e){
           JOptionPane.showMessageDialog(null, "Ocorreu um erro. Verifique se os campos foram digitados corretamente.");
        }

    }//GEN-LAST:event_estoqueBtnActionPerformed

